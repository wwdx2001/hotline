public class CallBillTabEntity {

    private String year;

    private String jd;

    private String cdesl;

    private String kzje;

    private String kzrq;

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getJd() {
        return jd;
    }

    public void setJd(String jd) {
        this.jd = jd;
    }

    public String getCdesl() {
        return cdesl;
    }

    public void setCdesl(String cdesl) {
        this.cdesl = cdesl;
    }

    public String getKzje() {
        return kzje;
    }

    public void setKzje(String kzje) {
        this.kzje = kzje;
    }

    public String getKzrq() {
        return kzrq;
    }

    public void setKzrq(String kzrq) {
        this.kzrq = kzrq;
    }
}
